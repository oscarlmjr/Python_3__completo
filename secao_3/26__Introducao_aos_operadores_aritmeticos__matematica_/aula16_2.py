"""
+, -, *, /, //, **, %, ()
"""
print('Divisão = 10 / 10 = ', 10 / 10)  # Divisão por Ponto Flutuante
print('Divisão Inteira = 15 // 2 = ', 15 // 2)  # Divisão Inteira
print('Exponenciação = 10 ** 2 = ', 10 ** 2)  # Exponenciação
print('Resto = 10 % 3 = ', 10 % 3)  # Resto
print('Alterar a precedência = (10 + 10) * 2 = ', (10 + 10) * 2)  # Alterar a precedência
